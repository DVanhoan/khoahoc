    public final void initTable() {
        tblModel = new DefaultTableModel();
        String[] headerTbl = new String[]{"Id", "Nội dung", "Người bình luận", "Bài học"};
        tblModel.setColumnIdentifiers(headerTbl);
        tblcomment.setModel(tblModel);
    }

    public void loadDataToTable(ArrayList<Comment> arr) {
        try {

            tblModel.setRowCount(0);
            for (Comment i : arr) {
                tblModel.addRow(new Object[]{
                    i.getId(), i.getContent(), i.getUser().getUserName(), i.getLesson().getId()
                });
            }
        } catch (Exception e) {
        }
    }

    public Comment getAccountSelect() throws SQLException {
        int i_row = tblcomment.getSelectedRow();
        int commentId = (int) tblcomment.getValueAt(i_row, 0);
        Comment comment = commentdao.selectById(commentId);
        return comment;
    }

