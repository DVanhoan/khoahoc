    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        try{
            String luachon = (String) cbxLuachon.getSelectedItem();
            String searchContent = txtSearch.getText();
            ArrayList<Comment> result = new ArrayList<>();
            switch (luachon) {
                case "Tất cả":
                    result = SearchComment.getInstance().searchTatCaAcc(searchContent);
                    break;
                case "nội dung":
                    result = SearchComment.getInstance().searchContent(searchContent);
                    break;
                case "Người bình luận":
                    result = SearchComment.getInstance().searchUser(searchContent);
                    break;
                case "Bài học":
                    result = SearchComment.getInstance().searchlesson(searchContent);
                    break;
            }
            loadDataToTable(result);
        }catch(Exception e){
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_txtSearchKeyReleased


