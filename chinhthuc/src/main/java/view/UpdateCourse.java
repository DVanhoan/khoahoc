    public void setAvatarCombobox() {
        DefaultComboBoxModel<ImageCustom> model = new DefaultComboBoxModel<>();
        ArrayList<ImageCustom> iconsData = ImageReader.getIcons("avatars", 50, 50);
        for (ImageCustom icon : iconsData) {
            model.addElement(icon);
        }
        ImageCustom imageCustom = new ImageCustom();
        imageCustom.setIcon(ImageReader.createImageIcon(this.course.getImage(), 100, 100));
        imageCustom.setPath(this.course.getImage());
        model.setSelectedItem(imageCustom);
        avatarCombobox = new JComboBox<>(model);
        avatarCombobox.setRenderer(new ImageCustomRenderer());
        avatarCombobox.setBounds(440, 35, 100, 100); // Adjust size and position as needed
        jPanel1.add(avatarCombobox); // Assuming jPanel1 is where you want to add the combobox
    }
    
    private void setUserCreate() {
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        ArrayList<Account> result = daoUser.selectTeacher();
        for (Account a : result) {
            if(a.getUserName() !=  course.getUser().getUserName()){
                model.addElement(a.getUserName());
            }
        }
        model.setSelectedItem(course.getUser().getUserName());
        cbxLuachon.setModel(model);
    }

