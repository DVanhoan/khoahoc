    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(txtMaSanPham.getText());
            String name = txtTenSanPham1.getText();
            String donGiaText = txtDonGia.getText();
            donGiaText = donGiaText.replaceAll("[^\\d.]", "");
            double donGia = Double.parseDouble(donGiaText);
            ImageCustom image = (ImageCustom) avatarCombobox.getSelectedItem();
            String imagePath = image.getPath();
            System.out.println(image);
            String mota = txtMoTa.getText();
            String username = cbxLuachon.getSelectedItem().toString();
            
            if(name.isEmpty() || donGiaText.isEmpty()){
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
            }
            else{
                Account user = daoUser.find(username);
                Course course = new Course(id, name, imagePath, donGiaText, donGia, user);
                daoCourse.update(course);
                JOptionPane.showMessageDialog(this, "Cập Nhật Thành công");
                this.setVisible(false);
            }
            
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

