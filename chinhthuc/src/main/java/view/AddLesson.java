    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(txtIdLesson.getText().trim());
            String title = txtTitle.getText().trim();
            String mota = txtmota.getText().trim();
            String khoahoc = cbxKhoaHoc.getSelectedItem().toString();
            if (title.equals("") || mota.equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin !", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            } else {
                
                Lesson lesson = new Lesson();
                lesson.setId(id);
                lesson.setTitle(title);
                lesson.setDescription(mota);
                Course course = daoCourse.find(khoahoc);
                String order = setOrderLesson(daoLesson.selectByIdCourse(course.getId()));
                lesson.setOrder(Integer.parseInt(order));
                lesson.setCourse(course);
                daoLesson.insert(lesson);
                parent.loadDataToTable(daoLesson.selectAll());
                this.dispose();
            }
        } catch (Exception ex) {
             ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

