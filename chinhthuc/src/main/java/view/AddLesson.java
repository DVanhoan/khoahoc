    public String createIdLT() {
        ArrayList<Lesson> lessons = daoLesson.selectAll();
        int maxId = 0;
        for (Lesson lesson : lessons) {
            int currentId = (lesson.getId());
            if (currentId > maxId) {
                maxId = currentId;
            }
        }
        return "" + (maxId + 1);
    }


    public String setOrderLesson(ArrayList<Lesson> arr) {
        int maxOrder = 0;
        for (Lesson lesson : arr) {
            int currentOrder = (lesson.getOrder());
            if (currentOrder > maxOrder) {
                maxOrder = currentOrder;
            }
        }
        return "" + (maxOrder + 1);
    }
    
    

    

