    public String createIdLT() throws SQLException {
        ArrayList<Comment> allcomment = CommentService.getInstance().selectAll();
        int maxId = 0;
        for (Comment comment : allcomment) {
            int currentId = (comment.getId());
            if (currentId > maxId) {
                maxId = currentId;
            }
        }
        return ""+(maxId + 1); 
    }
    


