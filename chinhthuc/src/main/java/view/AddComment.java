    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(txtId.getText());
            String content = txtcontent.getText();
            String lessonTitle = cb_lesson1.getSelectedItem().toString();
            String username = cb_user.getSelectedItem().toString();

            if (content.equals("") || username.equals("") || lessonTitle.equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin !", "Cảnh báo ", JOptionPane.WARNING_MESSAGE);
            } else {
                Lesson lesson = lessonService.selectByTitle(lessonTitle);
                Account user = account_dao.find(username);

                if (lesson == null) {
                    JOptionPane.showMessageDialog(this, "Lesson not found!", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                if (user == null) {
                    JOptionPane.showMessageDialog(this, "User not found!", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                Comment comment = new Comment(id, content, user, lesson);
                CommentService.getInstance().insert(comment);
                this.dispose();
                homeAcc.loadDataToTable(CommentService.getInstance().selectAll());


                txtId.setText(createIdLT());
                txtcontent.setText("");
                cb_user.setSelectedIndex(0);
                cb_lesson1.setSelectedIndex(0);
            }
        } catch (Exception ex) {
            Logger.getLogger(AddComment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1MouseClicked

